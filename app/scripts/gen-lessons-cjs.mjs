#!/usr/bin/env node
// Generates scripts/lessons-cjs.mjs from the TypeScript sources, so the SEO
// build step (which runs in plain Node and can't import .ts) always has an
// up-to-date, drift-proof mirror of the curriculum + challenges.
//
// Runs automatically as the first step of `npm run build` / `build:pages`.
// It strips the TS types with esbuild (already a dependency via Vite) and
// imports the real modules, then re-serializes the exact data the SEO script
// needs. Single source of truth = src/lib/lessons.ts + src/lib/data.ts.

import { build } from 'esbuild';
import { writeFileSync, mkdtempSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { tmpdir } from 'node:os';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = join(__dirname, '..', 'src', 'lib');
const OUT = join(__dirname, 'lessons-cjs.mjs');

// Bundle lessons.ts + data.ts (for CHALLENGES) to ESM in a temp dir, then
// import the compiled output to read the real arrays. We feed esbuild a small
// stdin entry whose resolveDir is src/lib, so the relative .ts imports resolve.
const tmp = mkdtempSync(join(tmpdir(), 'tm-lessons-'));
const bundleFile = join(tmp, 'bundle.mjs');

await build({
  stdin: {
    contents: `export { LESSONS } from './lessons';\nexport { CHALLENGES } from './data';\n`,
    resolveDir: SRC,
    sourcefile: 'gen-lessons-entry.mjs',
    loader: 'js',
  },
  bundle: true,
  format: 'esm',
  platform: 'node',
  outfile: bundleFile,
  logLevel: 'warning',
});

const mod = await import(pathToFileURL(bundleFile).href);
const { LESSONS, CHALLENGES } = mod;

// Only the fields the SEO script reads. (guide is included now so SEO landing
// pages can show the full multi-paragraph guide, not just the one-liner.)
const lessons = LESSONS.map(l => ({
  id: l.id, title: l.title, subtitle: l.subtitle,
  instructions: l.instructions, keys: l.keys,
  guide: l.guide || null, tip: l.tip || '',
}));
const challenges = CHALLENGES.map(c => ({ id: c.id, label: c.label, desc: c.desc }));

const banner = `// AUTO-GENERATED by scripts/gen-lessons-cjs.mjs — DO NOT EDIT BY HAND.
// Source of truth: src/lib/lessons.ts + src/lib/data.ts (CHALLENGES).
// Regenerated on every \`npm run build\`.
`;

const out = `${banner}
export const LESSONS = ${JSON.stringify(lessons, null, 2)};

export const CHALLENGES = ${JSON.stringify(challenges, null, 2)};
`;

writeFileSync(OUT, out, 'utf8');
console.log(`✓ Generated lessons-cjs.mjs (${lessons.length} lessons, ${challenges.length} challenges)`);
